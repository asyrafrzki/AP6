#include <math.h>
//fungsi tambah
int tambah(int a, int b){
    return a + b;
}
//fungsi kurang
int kurang(int a, int b){
    return a - b;
}
//fungsi kali
int kali(int a, int b){
    return a * b;
}
//fungsi bagi
int bagi(int a, int b){
    return a / b;
}
//mod
int modulo(int a, int b){
    return a % b;
}
//fungsi pangkat
int pangkat(int a, int b){
    return pow(a, b);
}